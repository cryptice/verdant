-- Listing: producer publishes available stems for sale
CREATE TABLE listing (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT       NOT NULL REFERENCES app_user(id) ON DELETE CASCADE,
    species_id          BIGINT       NOT NULL REFERENCES species(id) ON DELETE CASCADE,
    title               VARCHAR(255) NOT NULL,
    description         TEXT,
    quantity_available  INTEGER      NOT NULL CHECK (quantity_available >= 0),
    price_per_stem_sek  INTEGER     NOT NULL,
    available_from      DATE         NOT NULL,
    available_until     DATE         NOT NULL,
    image_url           TEXT,
    is_active           BOOLEAN      DEFAULT true,
    created_at          TIMESTAMPTZ  DEFAULT now(),
    updated_at          TIMESTAMPTZ  DEFAULT now()
);
CREATE INDEX idx_listing_user ON listing(user_id);
CREATE INDEX idx_listing_species ON listing(species_id);
CREATE INDEX idx_listing_active ON listing(is_active, available_from, available_until);

-- Market order: purchaser places order
CREATE TABLE market_order (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    purchaser_id    BIGINT       NOT NULL REFERENCES app_user(id) ON DELETE CASCADE,
    producer_id     BIGINT       NOT NULL REFERENCES app_user(id) ON DELETE CASCADE,
    status          VARCHAR(20)  NOT NULL DEFAULT 'PLACED',
    delivery_date   DATE,
    total_sek       INTEGER      NOT NULL DEFAULT 0,
    notes           TEXT,
    created_at      TIMESTAMPTZ  DEFAULT now(),
    updated_at      TIMESTAMPTZ  DEFAULT now()
);
CREATE INDEX idx_market_order_purchaser ON market_order(purchaser_id);
CREATE INDEX idx_market_order_producer ON market_order(producer_id);

-- Order item: line items in an order
CREATE TABLE order_item (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id            BIGINT       NOT NULL REFERENCES market_order(id) ON DELETE CASCADE,
    listing_id          BIGINT       NOT NULL REFERENCES listing(id) ON DELETE CASCADE,
    species_id          BIGINT       NOT NULL REFERENCES species(id) ON DELETE SET NULL,
    species_name        VARCHAR(255) NOT NULL,
    quantity            INTEGER      NOT NULL CHECK (quantity > 0),
    price_per_stem_sek  INTEGER     NOT NULL CHECK (price_per_stem_sek >= 0)
);
CREATE INDEX idx_order_item_order ON order_item(order_id);

CREATE INDEX idx_plant_event_type ON plant_event(plant_id, event_type);
CREATE INDEX idx_plant_user_season ON plant(user_id, season_id);
