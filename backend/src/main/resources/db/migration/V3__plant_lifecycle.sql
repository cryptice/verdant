-- Migrate existing species strings to species records and link them
-- (For any existing plants with a species name, create a species record and set species_id)
INSERT INTO species (user_id, common_name, created_at)
SELECT DISTINCT g.owner_id, p.species, now()
FROM plant p
JOIN bed b ON p.bed_id = b.id
JOIN garden g ON b.garden_id = g.id
WHERE p.species IS NOT NULL AND p.species != '';

UPDATE plant SET species_id = s.id
FROM species s, bed b, garden g
WHERE plant.bed_id = b.id
  AND b.garden_id = g.id
  AND s.user_id = g.owner_id
  AND s.common_name = plant.species
  AND plant.species IS NOT NULL AND plant.species != '';

