-- Plant event timeline
CREATE TABLE plant_event (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plant_id        BIGINT       NOT NULL REFERENCES plant(id) ON DELETE CASCADE,
    event_type      VARCHAR(30)  NOT NULL,
    event_date      DATE         NOT NULL DEFAULT CURRENT_DATE,
    plant_count     INT,
    weight_grams    DOUBLE PRECISION,
    quantity         INT,
    notes           TEXT,
    image_base64    TEXT,
    ai_suggestions  TEXT,
    created_at      TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);
CREATE INDEX idx_plant_event_plant ON plant_event(plant_id);

-- Frequent comments
CREATE TABLE frequent_comment (
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id   BIGINT NOT NULL REFERENCES app_user(id) ON DELETE CASCADE,
    text      VARCHAR(500) NOT NULL,
    use_count INT NOT NULL DEFAULT 1,
    UNIQUE(user_id, text)
);
CREATE INDEX idx_frequent_comment_user ON frequent_comment(user_id);
