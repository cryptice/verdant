-- Species groups (user_id NULL = system/global group)
CREATE TABLE species_group (
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id  BIGINT REFERENCES app_user(id) ON DELETE CASCADE,
    name     VARCHAR(255) NOT NULL
);
CREATE INDEX idx_species_group_user ON species_group(user_id);

-- Species (user_id NULL = system/global species)
CREATE TABLE species (
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id               BIGINT REFERENCES app_user(id) ON DELETE CASCADE,
    common_name           VARCHAR(255) NOT NULL,
    scientific_name       VARCHAR(255),
    image_base64          TEXT,
    days_to_sprout        INT,
    days_to_harvest       INT,
    germination_time_days INT,
    sowing_depth_mm       INT,
    growing_positions     VARCHAR(255),
    soils                 VARCHAR(255),
    height_cm             INT,
    bloom_time            VARCHAR(100),
    germination_rate      INT,
    group_id              BIGINT REFERENCES species_group(id) ON DELETE SET NULL,
    created_at            TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);
CREATE INDEX idx_species_user ON species(user_id);

-- Species tags (user_id NULL = system/global tag)
CREATE TABLE species_tag (
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id  BIGINT REFERENCES app_user(id) ON DELETE CASCADE,
    name     VARCHAR(100) NOT NULL
);
CREATE INDEX idx_species_tag_user ON species_tag(user_id);

CREATE TABLE species_tag_mapping (
    species_id BIGINT NOT NULL REFERENCES species(id) ON DELETE CASCADE,
    tag_id     BIGINT NOT NULL REFERENCES species_tag(id) ON DELETE CASCADE,
    PRIMARY KEY (species_id, tag_id)
);

